// Function to check if a number is prime using recursive method
func isPrimeRecursive(n, divisor int) bool {
	if n < 2 {
		return false
	}
	if divisor == 1 {
		return true
	}
	if n%divisor == 0 {
		return false
	}
	return isPrimeRecursive(n, divisor-1)
}

